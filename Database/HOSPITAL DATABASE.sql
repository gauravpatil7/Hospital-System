CREATE FUNCTION UFN_USERAPPOINTMENTS_BY_ID(@mailId varchar(30))
RETURNS TABLE
AS
RETURN(
SELECT D.DNAME as DoctorName ,A.APPONTMENTID as APPONTMENTID , A.APPOINTMENTTIME as AppointmentDT , H.HOSPITALNAME as HospitalName, H.HOSPITALADDRESS as HospitalAddress, H.CONTACT as HospitalContact
FROM DOCTORS D JOIN APPONTMENTS A ON D.DID = A.DOCTORID
JOIN HOSPITALL H ON H.HOSPITALID = A.HOSPITALID WHERE A.USERID = @mailId
);
GO


