CREATE PROCEDURE USP_INSERT_APPOINTMENTS
(
    @HOSPITALID INT,
    @USERID VARCHAR(20),
    @DOCTORID INT,
    @APPOINTMENTTIME DATETIME
)
AS
BEGIN
    DECLARE @PREVIOUS DATETIME = DATEADD(MINUTE, -30, @APPOINTMENTTIME);
    DECLARE @NEXT DATETIME = DATEADD(MINUTE, 30, @APPOINTMENTTIME);
    
    BEGIN TRY
        IF (@APPOINTMENTTIME < GETDATE()) 
        BEGIN 
            -- Return -1 for past appointment
            RETURN -1 
        END
        ELSE IF NOT EXISTS (SELECT MAILID FROM USERS WHERE MAILID = @USERID) 
        BEGIN 
            -- Return -2 for invalid user
            RETURN -2 
        END
        ELSE IF NOT EXISTS (SELECT HOSPITALID FROM HOSPITALL WHERE HOSPITALID = @HOSPITALID) 
        BEGIN 
            -- Return -3 for invalid hospital
            RETURN -3 
        END
        ELSE IF NOT EXISTS (SELECT DID FROM DOCTORS WHERE DID = @DOCTORID) 
        BEGIN 
            -- Return -4 for invalid doctor
            RETURN -4 
        END
        ELSE IF EXISTS (
            SELECT 1 
            FROM APPONTMENTS 
            WHERE USERID = @USERID 
                AND APPOINTMENTTIME BETWEEN @PREVIOUS AND @NEXT
        )
        BEGIN 
            -- Return -5 for user having another appointment within half an hour
            RETURN -5 
        END
        ELSE IF EXISTS (
            SELECT 1 
            FROM APPONTMENTS 
            WHERE DOCTORID = @DOCTORID 
                AND APPOINTMENTTIME BETWEEN @PREVIOUS AND @NEXT
        )
        BEGIN 
            -- Return -6 for doctor having another appointment within half an hour
            RETURN -6 
        END
        ELSE
        BEGIN
            -- Insert the appointment if all checks pass
            INSERT INTO APPONTMENTS (HOSPITALID, USERID, DOCTORID, APPOINTMENTTIME)
            VALUES (@HOSPITALID, @USERID, @DOCTORID, @APPOINTMENTTIME);
            
            -- Return 7 for successful insertion
            RETURN 7 
        END
    END TRY
    BEGIN CATCH
        -- Handle exceptions here
    END CATCH
END
GO


