APPONTMENTID INT CONSTRAINT PK_APPONTMENTID PRIMARY KEY IDENTITY(10001, 1),
HOSPITALID INT CONSTRAINT FK_HOSPITALID REFERENCES HOSPITALL(HOSPITALID),
